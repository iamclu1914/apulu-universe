def enrich_bars_with_hooks(catalog_lines):
    """Use Claude to generate ig_hook, x_post, tiktok_pov for local catalog bars."""
    from vawn_config import get_anthropic_client
    client = get_anthropic_client()

    bars_text = "\n".join([f"- \"{l['bar']}\" (from {l['track']}, {l['territory']})" for l in catalog_lines])

    resp = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=800,
        messages=[{"role": "user", "content": f"""For each bar below, generate:
1. ig_hook: Instagram hook under 15 words
2. x_post: X/Twitter post under 200 chars with 1-2 hashtags
3. tiktok_pov: starts with "POV:" + 3-5 words

Bars:
{bars_text}

Return ONLY JSON array:
[{{"bar": "...", "ig_hook": "...", "x_post": "...", "tiktok_pov": "..."}}]"""}],
    )

    try:
        text = resp.content[0].text
        start = text.find("[")
        end = text.rfind("]") + 1
        hooks = json.loads(text[start:end])
        for i, hook in enumerate(hooks):
            if i < len(catalog_lines):
                catalog_lines[i]["ig_hook"] = hook.get("ig_hook", "")
                catalog_lines[i]["x_post"] = hook.get("x_post", "")
                catalog_lines[i]["tiktok_pov"] = hook.get("tiktok_pov", "")
    except Exception as e:
        print(f"[CatalogAgent] Hook enrichment failed: {e}")

    return catalog_lines


