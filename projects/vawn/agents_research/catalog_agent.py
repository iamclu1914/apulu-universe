async def ensure_notebook():
    """Create or load the Vawn Catalog notebook in NotebookLM."""
    from notebooklm import NotebookLMClient

    state = load_json(NOTEBOOKLM_STATE)

    async with await NotebookLMClient.from_storage() as client:
        if "notebook_id" in state:
            try:
                nb = await client.notebooks.get(state["notebook_id"])
                print(f"[CatalogAgent] Using existing notebook: {nb.title} ({nb.id})")
                return state["notebook_id"]
            except Exception:
                print("[CatalogAgent] Stored notebook not found, creating new one")

        nb = await client.notebooks.create("Vawn Catalog")
        print(f"[CatalogAgent] Created notebook: {nb.id}")

        catalog_text = CATALOG_HTML.read_text(encoding="utf-8")
        source = await client.sources.add_text(
            nb.id,
            title="Vawn Full Lyrics Catalog",
            content=catalog_text,
            wait=True,
            wait_timeout=120.0,
        )
        print(f"[CatalogAgent] Added catalog source, waiting for processing...")

        state["notebook_id"] = nb.id
        save_json(NOTEBOOKLM_STATE, state)
        return nb.id


async def query_catalog(notebook_id, trend_angles):
    """Query NotebookLM for quotable bars matching today's trends."""
    from notebooklm import NotebookLMClient

    angles_str = "\n".join([f"- {a}" for a in trend_angles])

    query = f"""Given these social media content themes for today:
{angles_str}

Find 3 of the most quotable, scroll-stopping lines from Vawn's catalog.

For each line, give me:
1. The exact bar (word for word)
2. Which track
3. Territory (Fear of Failure, Dependability, Love, Journey)
4. A ready-to-post Instagram hook (under 15 words) that uses this bar
5. A ready-to-post X/Twitter post (under 200 chars) built around this bar
6. A TikTok POV line (starts with "POV:" + 3-5 words)

Pick bars that HIT on their own — no context needed to understand them."""

    async with await NotebookLMClient.from_storage() as client:
        result = await client.chat.ask(notebook_id, query)
        return result


def parse_catalog_response(response_text):
    """Parse NotebookLM response into structured catalog lines."""
    from vawn_config import get_anthropic_client
    client = get_anthropic_client()

    resp = client.messages.create(
        model="claude-sonnet-4-6",
        max_tokens=1200,
        messages=[{"role": "user", "content": f"""Parse this NotebookLM response about Vawn lyrics into JSON.

RESPONSE:
{response_text}

Return EXACTLY this JSON structure — no markdown fencing, raw JSON only:
{{
  "catalog_lines": [
    {{
      "bar": "exact quoted lyric line",
      "track": "TRACK NAME IN CAPS",
      "territory": "Fear of Failure|Dependability|Love|Journey",
      "ig_hook": "ready-to-post Instagram hook under 15 words",
      "x_post": "ready-to-post X/Twitter caption under 200 chars with 1-2 hashtags",
      "tiktok_pov": "POV: 3-5 words"
    }}
  ]
}}

Return 3 lines. Use only bars actually quoted. Generate the ig_hook, x_post, and tiktok_pov yourself if the response doesn't include them."""}],
    )

    text = resp.content[0].text
    try:
        if "```" in text:
            text = text.split("```")[1]
            if text.startswith("json"):
                text = text[4:]
            text = text.strip()
        return json.loads(text)
    except json.JSONDecodeError:
        return {"catalog_lines": []}


